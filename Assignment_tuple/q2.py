# 2. Write a Python program to create a tuple with different data types. 

t1 = (1, 2, 3)
print(f"Type:{type(t1)} ; tuple:{t1}")
t2 = (1.1, 2.2, 3.3)
print(f"Type:{type(t2)} ; tuple:{t2}")
t3 = ("s1", "s2", "s3")
print(f"Type:{type(t3)} ; tuple:{t3}")
t4 = (True, False)
print(f"Type:{type(t4)} ; tuple:{t4}")
t5 = (3+4j, 5-9j)
print(f"Type:{type(t5)} ; tuple:{t5}")
t6 = (None)
print(f"Type:{type(t6)} ; tuple:{t6}")
t7 = tuple([1,2,3,4])
print(f"Type:{type(t7)} ; tuple:{t7}")
t8 = tuple({1,2,3})
print(f"Type:{type(t8)} ; tuple:{t8}")
t9 = tuple({1:1111,2:2222,3:3333})
print(f"Type:{type(t9)} ; tuple:{t9}")
t10 = tuple(b"Hello")
print(f"Type:{type(t10)} ; tuple:{t10}")
t11 = tuple(bytearray(b"Hello"))
print(f"Type:{type(t11)} ; tuple:{t11}")
t12 = tuple(memoryview(b"Hello"))
print(f"Type:{type(t12)} ; tuple:{t12}")
t13 = tuple( frozenset([1, 2, 3]))
print(f"Type:{type(t13)} ; tuple:{t13}")